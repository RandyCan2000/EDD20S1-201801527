    public void CargarCantidadDeMonedas(){
        try{
            String[] ID=null;
            ID=jComboBox2.getSelectedItem().toString().split(";");
            int Monedas=0;
            for(int i=0;i<G.STATUS.size();i++){
                if(G.STATUS.get(i).getId_Moneda()==Integer.parseInt(ID[1]) &&
                    G.STATUS.get(i).getId_User()==G.Id_Usuario){
                        Monedas+=G.STATUS.get(i).getCantidad_Monedas();
                }
            }
            CM.setText(Integer.toString(Monedas));
        }catch(Exception E){
            CM.setText("0");
        }
    }

