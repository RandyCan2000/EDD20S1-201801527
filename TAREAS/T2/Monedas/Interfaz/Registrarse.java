    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int index=0;
        boolean existe=false;
        while(!G.USUARIOS.isEmpty()){
            if(G.USUARIOS.get(index).getUser().equals(jTextField1.getText())){existe=true;}
            if(index==G.USUARIOS.size()-1){break;}
            index++;
        }
        if(!jTextField1.getText().equals("") && !jPasswordField1.getText().equals("")&& existe==false){
            boolean rol=false;
            if(jComboBox1.getSelectedItem().equals("Usuario")){rol=false;}else{rol=true;}
            Usuarios nuevo=new Usuarios(Integer.parseInt(ID.getText()),jTextField1.getText(),jPasswordField1.getText(),rol);
            G.USUARIOS.add(nuevo);
            JOptionPane.showMessageDialog(null,"AGREGADO CON EXITO","AGREGADO",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            if(existe==true){
                JOptionPane.showMessageDialog(null,"USUARIO EXISTENTE CAMBIELO","",JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"CAMPOS VACIOS","FALTAN DATOS",JOptionPane.ERROR_MESSAGE);}
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

