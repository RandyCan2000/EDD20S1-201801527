    public void IniciarSesion(){
        int index=0;
        while(!G.USUARIOS.isEmpty()){
            if(G.USUARIOS.get(index).getUser().equals(User.getText())){
                if(G.USUARIOS.get(index).getPassword().equals(Password.getText())){
                    G.Rol=G.USUARIOS.get(index).isRol();
                    G.Id_Usuario=index;
                    System.out.println(G.Id_Usuario);
                    Banco.main(null);
                    this.setVisible(false);
                    break;
                }
            }
            if(index==G.USUARIOS.size()-1){
            JOptionPane.showMessageDialog(null,"USUARIO NO ENCONTRADO","USUARIO INEXISTENTE",JOptionPane.ERROR_MESSAGE);
            break;
            }
            index++;
        }
    }
    
