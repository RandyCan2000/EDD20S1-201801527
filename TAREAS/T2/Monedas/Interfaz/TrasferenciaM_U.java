    public void Transferencia(int ID_US2){
        try{
            String NMoneda=(String)jComboBox1.getSelectedItem();
            int Cantidad=(int)jSpinner1.getValue();
            for(int i=0;i<G.MONEDAS.size();i++){
                if(G.MONEDAS.get(i).getNombreMoneda().equals(NMoneda)){
                    if(G.MONEDAS.get(i).getCantidadMonedas()>=Cantidad && Cantidad>0){
                        Status Ingreso=new Status(ID_US2,G.MONEDAS.get(i).getId(),Cantidad);
                        G.STATUS.add(Ingreso);
                        G.MONEDAS.get(i).setCantidadMonedas(G.MONEDAS.get(i).getCantidadMonedas()-Cantidad);
                        CM.setText(Integer.toString(G.MONEDAS.get(i).getCantidadMonedas()));
                        JOptionPane.showMessageDialog(null, "Tranferencia Realizada con exito","Transferencia",JOptionPane.INFORMATION_MESSAGE);
                        break;
                    }else{
                        JOptionPane.showMessageDialog(null, "Cantidad Insuficiente de \n"
                                + "Fichas en Monedero","ERROR",JOptionPane.ERROR_MESSAGE);
                        break;
                    }
                }
            }
        }
        catch(Exception E){
            JOptionPane.showMessageDialog(null, "Ocurrio un error Corrovore los datos","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
    
