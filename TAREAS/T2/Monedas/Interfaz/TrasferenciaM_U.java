    public void CargarMoneda(){
        for(int i=0;i<G.MONEDAS.size();i++){
            if(G.MONEDAS.get(i).getCantidadMonedas()>0){
                jComboBox1.addItem(G.MONEDAS.get(i).getNombreMoneda());
            }
        }
    }

