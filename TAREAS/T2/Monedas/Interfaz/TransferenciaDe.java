    public void TRANSFERIR(int USER1,int USER2,int IdMoneda){
        int Monedas=0;
        for(int i=0;i<G.STATUS.size();i++){
            if(USER1==G.STATUS.get(i).getId_User() && IdMoneda==G.STATUS.get(i).getId_Moneda()){
                Monedas+=G.STATUS.get(i).getCantidad_Monedas();
            }
        }
        if(Monedas>=(int)jSpinner1.getValue()){
            Status Deposito=new Status(USER2,IdMoneda,(int)jSpinner1.getValue());
            System.out.println(USER2+" "+IdMoneda+" "+(int)jSpinner1.getValue());
            Status Retiro=new Status(USER1,IdMoneda,-(int)jSpinner1.getValue());
            System.out.println(USER1+" "+IdMoneda+" "+-(int)jSpinner1.getValue());
            G.STATUS.add(Deposito);
            G.STATUS.add(Retiro);
            JOptionPane.showMessageDialog(null,"TRANFERENCIA REALIZADA CON EXITO", "TRANSFERENCIA",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null,"MONEDAS INSUFICIENTES", "NO TRANSFERENCIA",JOptionPane.ERROR_MESSAGE);
        }
    }
    
